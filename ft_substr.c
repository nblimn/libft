#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	nlen;
	char	*sub;

	if (len > ft_strlen(s) + 1)
		nlen = ft_strlen(s) + 1;
	else
		nlen = len + 1;
	sub = (char *)malloc(sizeof(char) * (nlen));
	if (!sub)
		return (NULL);
	i = 0;
	j = 0;
	while (s[i])
	{
		if (i >= start && j < len)
		{
			sub[j] = s[i];
			j++;
		}
		i++;
	}
	sub[j] = 0;
	return (sub);
}